#!/usr/bin/env python3
"""Convert a DIA-NN spectral library to the OpenSWATH transition TSV format.

Both tools must search the *same* library, otherwise the comparison measures
library quality rather than extraction algorithm.

Two conversions here are not renames and will silently destroy the analysis if
missed:

1. **RT units.** DIA-NN reports retention time in MINUTES; mzML stores it in
   SECONDS, and OpenSWATH compares library RT against spectrum RT directly. A
   library left in minutes puts every peptide at 1/60th of its true elution
   time, and nothing is found. Converted explicitly, and reported.

2. **Decoys.** DIA-NN's empirical library carries only a handful of decoys
   (it has already used them), far too few for PyProphet to fit an error model.
   Targets only are emitted here; decoys must be generated by
   OpenSwathDecoyGenerator so OpenSWATH gets a decoy set matched to its own
   scoring.

Target column names come from TransitionTSVFile::header_names_ in OpenMS 3.6.

    diann_lib_to_openswath.py <lib.parquet> <out.tsv> [--rt-unit minutes|seconds]
"""
import sys

import pandas as pd

# OpenSWATH column <- first matching DIA-NN column. Covers both the dotted
# parquet schema and the older .tsv speclib names.
RENAME = {
    "PrecursorMz": ["Precursor.Mz", "PrecursorMz"],
    "ProductMz": ["Product.Mz", "ProductMz"],
    "PrecursorCharge": ["Precursor.Charge", "PrecursorCharge"],
    "ProductCharge": ["Fragment.Charge", "FragmentCharge", "ProductCharge"],
    "LibraryIntensity": ["Relative.Intensity", "LibraryIntensity"],
    "NormalizedRetentionTime": ["RT", "Tr_recalibrated", "RetentionTime", "iRT"],
    "PeptideSequence": ["Stripped.Sequence", "PeptideSequence", "StrippedPeptide"],
    "ModifiedPeptideSequence": ["Modified.Sequence", "ModifiedPeptide",
                                "FullUniModPeptideName"],
    "ProteinId": ["Protein.Group", "Protein.Ids", "ProteinGroup", "ProteinName"],
    "GeneName": ["Genes", "GeneName"],
    "FragmentType": ["Fragment.Type", "FragmentType"],
    "FragmentSeriesNumber": ["Fragment.Series.Number", "FragmentSeriesNumber"],
    "TransitionGroupId": ["Precursor.Id", "transition_group_id"],
    "Decoy": ["Decoy", "decoy"],
    "PrecursorIonMobility": ["IM", "IonMobility"],
}

REQUIRED = ["PrecursorMz", "ProductMz", "PrecursorCharge", "LibraryIntensity",
            "NormalizedRetentionTime", "PeptideSequence", "ProteinId"]


def convert(df, rt_scale=60.0):
    out = pd.DataFrame()
    for target, sources in RENAME.items():
        for s in sources:
            if s in df.columns:
                out[target] = df[s].values
                break

    missing = [c for c in REQUIRED if c not in out.columns]
    if missing:
        raise SystemExit(f"error: library lacks required columns: {missing}\n"
                         f"available: {sorted(df.columns)}")

    out["NormalizedRetentionTime"] = out["NormalizedRetentionTime"] * rt_scale

    # Targets only -- see module docstring.
    if "Decoy" in out.columns:
        out = out[out["Decoy"] == 0].copy()
    out["Decoy"] = 0

    if "ModifiedPeptideSequence" not in out.columns:
        out["ModifiedPeptideSequence"] = out["PeptideSequence"]
    if "TransitionGroupId" not in out.columns:
        out["TransitionGroupId"] = (out["ModifiedPeptideSequence"].astype(str)
                                    + "_" + out["PrecursorCharge"].astype(str))
    out["TransitionId"] = (out["TransitionGroupId"].astype(str) + "_"
                           + out.groupby("TransitionGroupId").cumcount().astype(str))

    out["DetectingTransition"] = 1
    out["IdentifyingTransition"] = 0
    out["QuantifyingTransition"] = 1
    return out


def selftest():
    df = pd.DataFrame({
        "Precursor.Mz": [500.1] * 3, "Product.Mz": [301.2, 402.3, 501.4],
        "Precursor.Charge": [2] * 3, "Fragment.Charge": [1] * 3,
        "Relative.Intensity": [1.0, 0.5, 0.2], "RT": [30.5] * 3,
        "Stripped.Sequence": ["SLYNTVATL"] * 3,
        "Modified.Sequence": ["SLYNTVATL"] * 3,
        "Protein.Group": ["P12345"] * 3, "Fragment.Type": ["y", "y", "b"],
        "Fragment.Series.Number": [3, 4, 5], "Precursor.Id": ["SLYNTVATL2"] * 3,
        "Decoy": [0, 0, 1],
    })
    out = convert(df)
    # minutes -> seconds is the conversion most likely to be silently wrong
    assert out["NormalizedRetentionTime"].iloc[0] == 30.5 * 60, out["NormalizedRetentionTime"]
    assert len(out) == 2, "decoy row must be dropped"
    assert (out["Decoy"] == 0).all()
    assert out["TransitionGroupId"].nunique() == 1
    assert out["TransitionId"].nunique() == 2
    assert all(c in out.columns for c in REQUIRED)
    # seconds passthrough
    assert convert(df, rt_scale=1.0)["NormalizedRetentionTime"].iloc[0] == 30.5
    print("selftest OK")


def main():
    if len(sys.argv) == 2 and sys.argv[1] == "--selftest":
        return selftest()
    if len(sys.argv) < 3:
        raise SystemExit(__doc__)
    src, dst = sys.argv[1], sys.argv[2]
    unit = sys.argv[4] if len(sys.argv) > 4 and sys.argv[3] == "--rt-unit" else "minutes"
    scale = 60.0 if unit == "minutes" else 1.0

    df = (pd.read_parquet(src) if src.endswith((".parquet", ".pq"))
          else pd.read_csv(src, sep="\t"))
    out = convert(df, rt_scale=scale)
    out.to_csv(dst, sep="\t", index=False)

    n = out["TransitionGroupId"].nunique()
    rt = out["NormalizedRetentionTime"]
    print(f"{len(df):,} rows -> {len(out):,} target transitions -> {dst}")
    print(f"{n:,} precursors, {len(out)/max(1,n):.1f} transitions each")
    print(f"RT treated as {unit}; written in seconds, range {rt.min():.0f}-{rt.max():.0f} s")
    print("decoys NOT included - generate with OpenSwathDecoyGenerator")


if __name__ == "__main__":
    main()
